class Bunga {
    public String nama;
    public String kelopak; 
    public String daun;
    public String tangkai;

    public String getNama(){
        return nama;
    }

    public void setNama(String newNama){
        this.nama = newNama;
    }
    
    public String getKelopak() {
        return kelopak;
    }

    public void setKelopak(String newKelopak) {
        this.kelopak = newKelopak;
    }
    
    public String getDaun() {
        return daun;
    }

    public void setDaun(String newDaun) {
        this.daun = newDaun;
    }

    public String getTangkai() {
        return tangkai;
    }
        
    public void setTangkai(String newTangkai) {
        this.tangkai = newTangkai;
    }
    
}